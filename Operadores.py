# Operadores Aritméticos

a = 2 + 3   # Soma
b = 2 - 3   # Subtração
c = 2 * 3   # Multiplicação
d = 2 / 3   # Divisão
e = 2 // 3  # Divisão inteira
f = 2 ** 3  # Potência
g = 2 % 3   # Resto da divisão

# Podemos fazer operações com variáveis numéricas e strings
nome = input('Digite seu primeiro nome: ')
nome = nome * 5
print(nome)


# Operadores Relacionais

comparacao1 = 5 > 3    # Maior que
comparacao2 = 5 < 3    # Menor que
comparacao3 = 5 >= 3   # Maior ou igual
comparacao4 = 5 <= 3   # Menor ou igual
comparacao5 = 5 == 3   # Igual
comparacao6 = 5 != 3   # Diferente

print("comparacao1:", comparacao1)
print("comparacao2:", comparacao2)
print("comparacao3:", comparacao3)
print("comparacao4:", comparacao4)
print("comparacao5:", comparacao5)
print("comparacao6:", comparacao6)


# Operadores Lógicos

comparacao01 = 5 > 3 and 6 > 3   # verdadeiro se condição 1 for verdadeira E condição 2 for verdadeira
comparacao02 = 5 < 3 or 6 < 3   # verdadeiro se condição 1 for verdadeira OU condição 2 for verdadeira

print("comparacao01:", comparacao01)
print("comparacao02:", comparacao02)

